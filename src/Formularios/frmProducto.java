    private void jtproductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtproductoMouseClicked
        // TODO add your handling code here:
        try{
            filaseleccionada = jtproducto.getSelectedRow();
            if(filaseleccionada == -1){
                JOptionPane.showMessageDialog(this,"No se ha seleccionado ninguna fila","Mensaje del Sistema",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                DefaultTableModel modelotabla = (DefaultTableModel) jtproducto.getModel();

                String id = jtproducto.getValueAt(filaseleccionada,0).toString();
                String nombre = jtproducto.getValueAt(filaseleccionada,1).toString();
                String categoria = jtproducto.getValueAt(filaseleccionada,2).toString();
                String precio = jtproducto.getValueAt(filaseleccionada,3).toString(); //No se obtendra el valor de utilidad            

                txtidproduc.setText(id);
                txtnompro.setText(nombre);
                cbocatpro.setSelectedItem(categoria);
                
                txtprepro.setText(precio);            

                //this.bloquearEstados(true);
                this.btnNuevo.setEnabled(false);
                this.btnGuardar.setEnabled(false);
                this.btnModificar.setEnabled(true);
                this.btnCancelar.setEnabled(true);
                this.txtidproduc.setEnabled(true);
                this.txtnompro.setEnabled(true);
                this.cbocatpro.setEnabled(true);
                this.txtprepro.setEnabled(true);
                tapProducto.setSelectedIndex(1);
                txtidproduc.requestFocus();
                JOptionPane.showMessageDialog(this,"¡Producto encontrado!","Mensaje del Sistema",JOptionPane.INFORMATION_MESSAGE);
            }
        }catch (HeadlessException ex){
            JOptionPane.showMessageDialog(this,"Error" + ex + "\nPor favor inténtelo nuevamente","Mensaje del Sistema",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jtproductoMouseClicked
    public void filtro(String filtro, JTable jtableBuscar){
        dtmProducto = (DefaultTableModel) jtableBuscar.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(dtmProducto);
        jtableBuscar.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(filtro));
    }
    
