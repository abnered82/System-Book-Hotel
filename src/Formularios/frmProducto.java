    public void listarTable(){        
        dtmProducto.setRowCount(0);
        for(int i=0;i<sp.tamaño();i++){ 
            Object array[] = new Object[11];
            array[0] = sp.obtener(i).getId_pro(); 
            array[1] = sp.obtener(i).getNombre(); 
            array[2] = sp.obtener(i).getCatpro(); 
            array[3] = sp.obtener(i).getPrepro();
            dtmProducto.addRow(array); 
        }        
        jtproducto.setModel(dtmProducto);               
    }
    
