    public String ValidarDatos(){
        String mensajeValidarDatos = "";
        if(txtcliente.getText().equals("")){
            mensajeValidarDatos += "\nPor favor ingrese los datos del Cliente";
            txtcliente.requestFocus();
        } 
        if(txtproducto.getText().equals("")){
            mensajeValidarDatos += "\nPor favor ingrese los datos del Producto";
            txtproducto.requestFocus();
        } 
        if(txtcantidadpro.getText().equals("")){
            mensajeValidarDatos += "\nPor favor ingrese los datos del Producto";
            txtcantidadpro.requestFocus();
        }                   
        if(txttotalpro.getText().equals("")){
            mensajeValidarDatos += "\nPor favor Opere el producto";
            txttotalpro.requestFocus();
        }                          
        if(txtcantidaddias.getText().equals("")){
            mensajeValidarDatos += "\nPor favor ingrese la cantidad de dias de la habitacion";
            txtcantidaddias.requestFocus();
        }
        if(txttotalhabi.getText().equals("")){
            mensajeValidarDatos += "\nPor favor opere los datos de la habitacion";
        }
        return mensajeValidarDatos;              
    }  
    
    
    
